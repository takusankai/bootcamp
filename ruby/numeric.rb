# 整数の例
integer_1 = 10
integer_2 = 20
integer_3 = integer_1 + integer_2
puts "#{integer_3}"

# 浮動小数点数の例
float_1 = 3.14
float_2 = 1.414
float_3 = float_1 - float_2
puts "#{float_3}"

# 有理数の例
rational_1 = Rational(2, 3)
rational_2 = Rational(1, 4)
rational_3 = rational_1 * rational_2
puts "#{rational_3}"

# 複素数の例
complex_1 = Complex(1, 2)
complex_2 = Complex(3, 4)
complex_3 = complex_1 / complex_2
puts "#{complex_3}"
