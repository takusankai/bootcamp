# 整数の例
integer1 = 10
integer2 = 20
integer3 = integer1 + integer2
puts "#{integer3}"

# 浮動小数点数の例
float1 = 3.14
float2 = 1.414
float3 = float1 - float2
puts "#{float3}"

# 有理数の例
rational1 = Rational(2, 3)
rational2 = Rational(1, 4)
rational3 = rational1 * rational2
puts "#{rational3}"

# 複素数の例
complex1 = Complex(1, 2)
complex2 = Complex(3, 4)
complex3 = complex1 / complex2
puts "#{complex3}"
